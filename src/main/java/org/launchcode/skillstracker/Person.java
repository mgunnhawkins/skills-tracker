package org.launchcode.skillstracker;

public class Person {
    public String name;
    public String firstLanguage;
    public String secondLanguage;
    public String thirdLanguage;

    public Person(String name, String firstLanguage, String secondLanguage, String thirdLanguage) {
        this.name = name;
        this.firstLanguage = firstLanguage;
        this.secondLanguage = secondLanguage;
        this.thirdLanguage = thirdLanguage;
    }
}
